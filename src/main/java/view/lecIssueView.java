    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        String iid = txtid.getText();
        String email = lblmail.getText();
        String sid = lblsid.getText();
        String pro = txtprob.getText();
        String sol = txtsol.getText();
        String Type = lbltype.getText();
        String status = cmbstatus.getSelectedItem().toString();
        int id = Integer.parseInt(iid);
        //String email=lblmail.getText();
        
        //Notification
        String from = USER_NAME;
        String pass = PASSWORD;

        String[] to = {email};
        String subject = Type;
        String body = sol;
        
        objModel = objController.addIssue(sol, email, status);
        boolean result = objController.UpdateIssueToDB(objModel, id);

        if (result) {
            JOptionPane.showMessageDialog(rootPane, " Course have been Added to DB Success", "Info", 1);
            lblsid.setText("");
            lbldate.setText("");
            lbltype.setText("");
            lblmail.setText("");
            txtid.setText("");
            txtsol.setText("");
            txtprob.setText("");

            cmbstatus.setSelectedIndex(0);

            lblsid.setEnabled(true);
            lbldate.setEnabled(true);
            lbltype.setEnabled(true);
            lblmail.setEnabled(true);
            txtid.setEnabled(true);
            txtprob.setEnabled(true);

            txtid.requestFocus();

            Issue_Load();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error adding to db", "Error", 0);

        }
        // TODO add your handling code here:
            JOptionPane.showMessageDialog(rootPane, "Message Sending to "+email, "Sending", 1);

        addIssueController.sendFromGMail(from, pass,to, subject, body);


    }//GEN-LAST:event_btnaddActionPerformed

