    public void Issue_Load() {
        int c;
        try {
            pst = con.prepareStatement("select * from issues where type= 'Exam Issue';");

            rs = pst.executeQuery();

            ResultSetMetaData rsd = (ResultSetMetaData) rs.getMetaData();
            c = rsd.getColumnCount();

            d = (DefaultTableModel) tbliss.getModel();
            d.setRowCount(0);

            while (rs.next()) {
                Vector v2 = new Vector();

                for (int i = 1; i <= c; i++) {
                    v2.add(rs.getString("id"));

                    v2.add(rs.getString("sid"));
                    v2.add(rs.getString("date"));
                    v2.add(rs.getString("email"));

                    v2.add(rs.getString("type"));
                    v2.add(rs.getString("msg"));

                    v2.add(rs.getString("status"));

                }
                d.addRow(v2);

            }

        } catch (SQLException ex) {
            Logger.getLogger(allissuesStudent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

